
public class Demo2 {
//浮点数据类型：float和double
//double使用最为广泛，精度高，取值范围大
//浮点常数默认就double类型，如果给float赋值，
//浮点常数末尾必须加上f或F，把浮点常数由double
//类型转化为float类型
  public static void main(String[] args)
  {
	float numFloat = 10.1F;
	System.out.println("numFloat="+numFloat);
	double numDouble = 10.1;
	System.out.println("numDouble="+numDouble);
  }
	
}

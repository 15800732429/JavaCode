
public class Demo5 {
//定义两个变量，num1，num2，把100赋值给num1，把
//200赋值给num2，然后分别输出num1的值和num2的值
  public static void main(String[] args)
  {
//定义多个相同类型的变量，变量名之间用“，”分割
	int num1,num2;
	num1 = 100;
	num2 = 200;
//输出num1和num2的值
	System.out.println("num1 = "+num1);
	System.out.println("num2 = "+num2);
//可以在定义变量的同时，给变量赋值
//格式：数据类型 变量名=变量的值；
	int num3=900,num5=1000,num6=10;
	System.out.println("num3 = "+num3);
	System.out.println("num5 = "+num5);
	System.out.println("num6 = "+num6);
  }
}
